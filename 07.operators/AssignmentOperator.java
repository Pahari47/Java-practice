public class AssignmentOperator {
    public static void main(String[] args) {
        int a = 10;
        // a = a + 10;
        a += 10; // This is the assignment operator
        int b = 20;
        b -= 5; // Subtracting 5 from b
        System.out.println(a);
        System.out.println(b);
    }
}
